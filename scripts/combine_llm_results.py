#!/usr/bin/env python3
"""Combine per-model real-LLM eval outputs into cross-model tables.

Reads results/llm/<model>/llm_runs.json (lists of RunResult summary rows),
computes the §9.1 metrics grouped by model and by model×attack, runs the §9.2
Fisher/Holm/CI comparison of minimal_defense vs none per model, and writes
combined CSV files plus a human-readable RESULTS.md.

    python3 scripts/combine_llm_results.py results/llm
"""
import csv
import json
import pathlib
import sys

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[1]))
from pdam.evaluator import compare, holm  # noqa: E402


def mean(xs):
    xs = list(xs)
    return sum(xs) / len(xs) if xs else 0.0


def metrics(rows):
    n = len(rows)
    bt = sum(r["benign_total"] for r in rows)
    bs = sum(r["benign_success"] for r in rows)
    bb = sum(r["benign_blocked"] for r in rows)
    return {
        "n": n,
        "asr": mean(r["success"] for r in rows),
        "delayed_asr": mean(r["success"] and r["delayed"] for r in rows),
        "unauth_action_rate": mean(r["executed"] for r in rows),
        "exfiltration_rate": mean(r["exfiltration"] for r in rows),
        "trigger_precision": mean(
            1.0 if (r["fired_on_probe"] and not r["fired_on_noise"]) else 0.0
            for r in rows),
        "benign_task_success": (bs / bt) if bt else 1.0,
        "false_positive_rate": (bb / bt) if bt else 0.0,
        "state_lineage_recovery": mean(r["lineage_recoverable"] for r in rows),
    }


def load(root):
    rows = []
    for p in sorted(pathlib.Path(root).glob("*/llm_runs.json")):
        rows += json.loads(p.read_text())
    return rows


def group(rows, keys):
    out = {}
    for r in rows:
        out.setdefault(tuple(r[k] for k in keys), []).append(r)
    return out


def write_csv(path, table, cols):
    with open(path, "w", newline="") as fh:
        w = csv.DictWriter(fh, fieldnames=cols)
        w.writeheader()
        for row in table:
            w.writerow({c: (f"{row[c]:.3f}" if isinstance(row[c], float) else row[c])
                        for c in cols})


def main(root):
    rows = load(root)
    if not rows:
        print(f"no results found under {root}/*/llm_runs.json")
        return
    models = sorted({r["model"] for r in rows})

    # by model × defense
    md = []
    for (model, defense), grp in sorted(group(rows, ["model", "defense"]).items()):
        md.append({"model": model, "defense": defense, **metrics(grp)})
    cols = ["model", "defense", "n", "asr", "delayed_asr", "unauth_action_rate",
            "exfiltration_rate", "trigger_precision", "benign_task_success",
            "false_positive_rate", "state_lineage_recovery"]
    write_csv(f"{root}/combined_by_model_defense.csv", md, cols)

    # by model × attack × defense
    mad = []
    for (model, at, defense), grp in sorted(group(rows, ["model", "attack_type", "defense"]).items()):
        mad.append({"model": model, "attack_type": at, "defense": defense, **metrics(grp)})
    write_csv(f"{root}/combined_by_model_attack_defense.csv", mad,
              ["model", "attack_type", "defense", "n", "asr", "exfiltration_rate",
               "unauth_action_rate", "state_lineage_recovery"])

    # RESULTS.md
    lines = ["# Real-LLM evaluation results", "",
             "Generated by `scripts/run_llm_eval.sh` + `scripts/combine_llm_results.py`.",
             "Planner = a real open-weight model served by LM Studio; each emitted "
             "tool call is judged mechanically (data-flow / policy), not by keywords.",
             "", "## ASR and utility by model × defense", "",
             "| model | defense | n | ASR | delayed | unauth | exfil | trig.P | benign | FPR | lineage |",
             "|---|---|--:|--:|--:|--:|--:|--:|--:|--:|--:|"]
    for r in md:
        lines.append("| {model} | {defense} | {n} | {asr:.3f} | {delayed_asr:.3f} | "
                     "{unauth_action_rate:.3f} | {exfiltration_rate:.3f} | "
                     "{trigger_precision:.3f} | {benign_task_success:.3f} | "
                     "{false_positive_rate:.3f} | {state_lineage_recovery:.3f} |".format(**r))

    lines += ["", "## minimal_defense vs none (Fisher exact, 95% CI) per model", ""]
    for model in models:
        none = [r for r in rows if r["model"] == model and r["defense"] == "none"]
        mind = [r for r in rows if r["model"] == model and r["defense"] == "minimal_defense"]
        if not none or not mind:
            continue
        a_s = sum(r["success"] for r in mind)
        b_s = sum(r["success"] for r in none)
        c = compare(f"{model}", a_s, len(mind), b_s, len(none))
        holm([c])
        lines.append(
            f"- **{model}**: none ASR={b_s}/{len(none)}={b_s/len(none):.3f}, "
            f"minimal ASR={a_s}/{len(mind)}={a_s/len(mind):.3f} "
            f"[{c.a_ci[0]:.3f},{c.a_ci[1]:.3f}]  "
            f"RD={c.risk_diff:+.3f} [{c.rd_ci[0]:+.3f},{c.rd_ci[1]:+.3f}]  "
            f"p={c.p_value:.3g}")

    pathlib.Path(f"{root}/RESULTS.md").write_text("\n".join(lines) + "\n")
    print("\n".join(lines))
    print(f"\nwrote combined CSVs + RESULTS.md under {root}/")


if __name__ == "__main__":
    main(sys.argv[1] if len(sys.argv) > 1 else "results/llm")
